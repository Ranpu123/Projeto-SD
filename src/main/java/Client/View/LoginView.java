    private void entrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarBtnActionPerformed
        String email = emailInput.getText();
        String senha = new String(senhaInput.getPassword());
        JFrame cur = this;
        JFrame currentFrame = this;
        EventQueue.invokeLater(new Runnable() {
            @Override
            public void run() {
                HandleRequest.getInstance().makeRequest(new LoginRequest(email, senha), (Response<?> response) -> {
                    if (!(response instanceof LoginResponse)) {
                        return;
                    }
                    Session.getInstance().setToken(((LoginResponse) response).payload().getToken());
                    
                    MainView main = new MainView();
                    main.setVisible(true);
                    cur.dispose();
                    
                }, errorLabel::setText);
            }
        });
    }//GEN-LAST:event_entrarBtnActionPerformed

